function clicar() {
    var a = document.getElementById("area");
    a.innerHTML = "Clicou!";
    a.style.background = "red";
}

function entrar() {
    var a = document.getElementById("area");
    a.innerHTML = "Entrou!";
}
function sair() {
    var a = document.getElementById("area");
    a.innerHTML = "Saiu!";
}
